Fahrenheit = int(input("Type the temperature in °F:\n \n"))
Celsius = int((Fahrenheit -32)/1.8) #The Fahrenheit scale was created by the European physicist Daniel Gabriel Fahrenheit (1686–1736).
#The Celsius scale was created by the astronomer, physicist, and geophysicist Anders Celsius (1701-1744).
print (f"\nThe temperature in Celcius is {Celsius}°C.")